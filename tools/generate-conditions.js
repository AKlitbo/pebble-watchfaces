#!/usr/bin/env node
/**
 * Generates the C weather-condition lookups from the shared vocabulary.
 *
 * Reads lib/js/weather/conditions.js (the single source of truth) and writes
 *   lib/c/pebble/ui/weather/icons_table.h            (token  -> icon resource)
 *   lib/c/pebble/ui/weather/labels_table.h           (token  -> short/long display label)
 *   lib/c/pebble/ui/weather/conditions_dev_table.h   (dev-only: every token, for the icon walk)
 *
 * Pure fs - no npm deps - so each face's wscript can run it under the SDK's node
 * with no node_modules install.
 *
 * Run via `npm run gen:conditions`, or automatically by each face's build.
 */
'use strict';

const fs = require('fs');
const path = require('path');

const WEATHER_DIR = path.resolve(__dirname, '..', 'lib', 'c', 'pebble', 'ui', 'weather');
const ICONS_OUT = path.join(WEATHER_DIR, 'icons_table.h');
const LABELS_OUT = path.join(WEATHER_DIR, 'labels_table.h');
const CONDITIONS_DEV_OUT = path.join(WEATHER_DIR, 'conditions_dev_table.h');

// fallback can be a bare resource string or an object carrying the resource plus
// its labels. handle both so older callers and specs keep working
function fallbackResourceOf(fallback) {
  return typeof fallback === 'string' ? fallback : fallback.resource;
}

function bannerLines() {
  return [
    '//------------------------------------------------------------------------------',
    '// <auto-generated>',
    '//     This code was generated by tools/generate-conditions.js.',
    '//     Source: lib/js/weather/conditions.js',
    '//',
    '//     Changes to this file may be lost if the code is regenerated.',
    '//     Edit the source vocabulary and run `npm run gen:conditions` instead.',
    '// </auto-generated>',
    '//------------------------------------------------------------------------------',
    '#pragma once',
    ''
  ];
}

/**
 * Builds the C icon-lookup source from a condition vocabulary.
 *
 * Pure string transform so it can be tested without touching the filesystem.
 * For each condition emits a token -> resource branch, and when the row has a
 * nightResource also emits a "<token>_NIGHT" -> nightResource branch (the wire
 * carries the _NIGHT token after dark).
 *
 * @param {{fallback: (string|{resource: string}), conditions: {token: string, resource: string, nightResource?: string}[]}} vocabulary
 * @return {string}
 */
function buildIconsTable({ fallback, conditions }) {
  const fallbackResource = `RESOURCE_ID_ICON_${fallbackResourceOf(fallback)}`;

  const lines = bannerLines();
  lines.push(
    `// resolves a condition token to its icon resource, falling back to ${fallbackResourceOf(fallback)}`,
    'static uint32_t wx_resource_for_table(const char *condition)',
    '{',
    '    if (!condition)',
    '    {',
    `        return ${fallbackResource};`,
    '    }',
    ''
  );

  for (const { token, resource, nightResource } of conditions) {
    lines.push(`    if (!strcmp(condition, "${token}"))`);
    lines.push('    {');
    lines.push(`        return RESOURCE_ID_ICON_${resource};`);
    lines.push('    }');
    lines.push('');

    if (nightResource) {
      lines.push(`    if (!strcmp(condition, "${token}_NIGHT"))`);
      lines.push('    {');
      lines.push(`        return RESOURCE_ID_ICON_${nightResource};`);
      lines.push('    }');
      lines.push('');
    }
  }

  lines.push(`    return ${fallbackResource};`);
  lines.push('}');
  lines.push('');

  return lines.join('\n');
}

/**
 * Builds one C label-lookup function (short or long) from the vocabulary.
 *
 * Emits a token -> label branch per condition, plus a "<token>_NIGHT" branch
 * (reusing the day label) for any night-capable row, so the C side never has to
 * strip the night marker before looking a label up. Pure string transform.
 *
 * @param {Object} vocabulary
 * @param {string} fnName C function name to emit.
 * @param {string} field  'labelShort' or 'labelLong'.
 * @param {string} fallbackText label returned for an unrecognized/null token.
 * @return {string[]} source lines (no banner).
 */
function buildLabelFn({ conditions }, fnName, field, fallbackText) {
  const lines = [
    `static const char *${fnName}(const char *condition)`,
    '{',
    '    if (!condition)',
    '    {',
    `        return "${fallbackText}";`,
    '    }',
    ''
  ];

  for (const condition of conditions) {
    const label = condition[field];
    lines.push(`    if (!strcmp(condition, "${condition.token}"))`);
    lines.push('    {');
    lines.push(`        return "${label}";`);
    lines.push('    }');
    lines.push('');

    if (condition.nightResource) {
      lines.push(`    if (!strcmp(condition, "${condition.token}_NIGHT"))`);
      lines.push('    {');
      lines.push(`        return "${label}";`);
      lines.push('    }');
      lines.push('');
    }
  }

  lines.push(`    return "${fallbackText}";`);
  lines.push('}');
  lines.push('');

  return lines;
}

/**
 * Builds the C label-lookup source (both short and long functions).
 *
 * @param {Object} vocabulary
 * @return {string}
 */
function buildLabelsTable(vocabulary) {
  const { fallback } = vocabulary;
  const shortFallback = typeof fallback === 'string' ? 'UNKNOWN' : fallback.labelShort;
  const longFallback = typeof fallback === 'string' ? 'Unknown' : fallback.labelLong;

  const lines = bannerLines();
  lines.push('#include <string.h>', '');
  lines.push(...buildLabelFn(vocabulary, 'wx_label_short_for_table', 'labelShort', shortFallback));
  lines.push(...buildLabelFn(vocabulary, 'wx_label_long_for_table', 'labelLong', longFallback));

  return lines.join('\n');
}

/**
 * Builds the dev-only condition-token array used by the icon walk: every token, with each
 * night form adjacent to its day form so consecutive taps compare the pair, then one bogus
 * token to exercise the WI_NA fallback. Pure string transform.
 *
 * @param {Object} vocabulary
 * @return {string}
 */
function buildDevConditionsTable({ conditions }) {
  const lines = bannerLines();
  lines.push(
    '// every condition token (day then night, adjacent) plus one bogus token that exercises',
    "// the WI_NA fallback. dev-only - a face names this only from inside its own #if DEV_MODE,",
    '// so a release build never links it. the icon walk taps through the list in order.',
    'static const char *const wx_dev_condition_tokens[] = {'
  );

  for (const { token, nightResource } of conditions) {
    lines.push(nightResource ? `    "${token}", "${token}_NIGHT",` : `    "${token}",`);
  }

  lines.push('    "ZZZ",  // unrecognised, exercises the WI_NA fallback');
  lines.push('};');
  lines.push('');

  return lines.join('\n');
}

function main() {
  const vocabulary = require('../lib/js/weather/conditions');
  const root = path.resolve(__dirname, '..');

  fs.writeFileSync(ICONS_OUT, buildIconsTable(vocabulary));
  fs.writeFileSync(LABELS_OUT, buildLabelsTable(vocabulary));
  fs.writeFileSync(CONDITIONS_DEV_OUT, buildDevConditionsTable(vocabulary));

  console.log(`generated ${path.relative(root, ICONS_OUT)}, ${path.relative(root, LABELS_OUT)} and ${path.relative(root, CONDITIONS_DEV_OUT)} (${vocabulary.conditions.length} conditions)`);
}

if (require.main === module) {
  main();
}

module.exports = { buildIconsTable, buildLabelsTable, buildDevConditionsTable };
