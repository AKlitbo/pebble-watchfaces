#!/usr/bin/env node
/**
 * Generates the C weather-icon lookup from the shared condition vocabulary.
 *
 * Reads lib/js/weather/conditions.js (the single source of truth) and writes
 * lib/c/pebble/weather/icons_table.h. Pure fs - no npm deps - so each face's
 * wscript can run it under the SDK's node with no node_modules install.
 *
 * Run via `npm run gen:conditions`, or automatically by each face's build.
 */
'use strict';

const fs = require('fs');
const path = require('path');

const OUT = path.resolve(__dirname, '..', 'lib', 'c', 'pebble', 'weather', 'icons_table.h');

/**
 * Builds the C icon-lookup source from a condition vocabulary.
 *
 * Pure string transform so it can be tested without touching the filesystem.
 *
 * @param {{fallback: string, conditions: {token: string, resource: string}[]}} vocabulary
 * @return {string}
 */
function buildIconsTable({ fallback, conditions }) {
  const fallbackResource = `RESOURCE_ID_ICON_${fallback}`;

  const lines = [
    '//------------------------------------------------------------------------------',
    '// <auto-generated>',
    '//     This code was generated by tools/generate-conditions.js.',
    '//     Source: lib/js/weather/conditions.js',
    '//',
    '//     Changes to this file may be lost if the code is regenerated.',
    '//     Edit the source vocabulary and run `npm run gen:conditions` instead.',
    '// </auto-generated>',
    '//------------------------------------------------------------------------------',
    '#pragma once',
    '',
    `// resolves a condition token to its icon resource, falling back to ${fallback}`,
    'static uint32_t wx_resource_for_table(const char *condition)',
    '{',
    '    if (!condition)',
    '    {',
    `        return ${fallbackResource};`,
    '    }',
    ''
  ];

  for (const { token, resource } of conditions) {
    lines.push(`    if (!strcmp(condition, "${token}"))`);
    lines.push('    {');
    lines.push(`        return RESOURCE_ID_ICON_${resource};`);
    lines.push('    }');
    lines.push('');
  }

  lines.push(`    return ${fallbackResource};`);
  lines.push('}');
  lines.push('');

  return lines.join('\n');
}

function main() {
  const vocabulary = require('../lib/js/weather/conditions');

  fs.writeFileSync(OUT, buildIconsTable(vocabulary));
  console.log(`generated ${path.relative(path.resolve(__dirname, '..'), OUT)} (${vocabulary.conditions.length} conditions)`);
}

if (require.main === module) {
  main();
}

module.exports = { buildIconsTable };
